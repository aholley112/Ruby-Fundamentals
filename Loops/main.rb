# Let's get Loopy!

# Generic Loop (is not used to often)
i = 0
loop do
  puts "i is #{i}"
  i += 1
  break if i == 10
end

# While Loop

while i < 10 do 
  puts "i is #{i}"
  i += 1 
end

# Until Loop

until i == 10 do
  puts "i is #{i}"
    i += 1 
  end

# Ranges
(1..10) # 1, 2, 3, 4, ...
